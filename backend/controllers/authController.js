const User = require('../models/User');
const { generateToken, generateOTP } = require('../utils/helpers');
const { sendOTPEmail, sendPasswordResetEmail } = require('../utils/email');
const crypto = require('crypto');

// @desc    Register user
// @route   POST /api/auth/register
// @access  Public
exports.register = async (req, res) => {
  try {
    const { email, phone, password } = req.body;

    // Validation
    if (!email || !phone || !password) {
      return res.status(400).json({
        success: false,
        message: 'Please provide all required fields'
      });
    }

    // Check if user exists
    const existingUser = await User.findOne({ $or: [{ email }, { phone }] });
    if (existingUser) {
      return res.status(400).json({
        success: false,
        message: 'User already exists with this email or phone'
      });
    }

    // Generate OTP
    const otp = generateOTP();
    const otpExpire = new Date(Date.now() + (process.env.OTP_EXPIRE_MINUTES || 10) * 60 * 1000);

    // Create user
    const user = await User.create({
      email,
      phone,
      password,
      otp: {
        code: otp,
        expiresAt: otpExpire
      }
    });

    // Log OTP to console for development (since email is not configured)
    console.log('\n' + '='.repeat(50));
    console.log('ðŸ” OTP VERIFICATION CODE');
    console.log('='.repeat(50));
    console.log(`Email: ${email}`);
    console.log(`OTP: ${otp}`);
    console.log(`Expires at: ${otpExpire.toLocaleString()}`);
    console.log('='.repeat(50) + '\n');

    // Try to send OTP email (will fail if email not configured, but won't block registration)
    try {
      await sendOTPEmail(email, otp);
      console.log('âœ… OTP email sent successfully');
    } catch (emailError) {
      console.log('âš ï¸  Email not configured. Using console OTP for development.');
    }

    res.status(201).json({
      success: true,
      message: 'Registration successful. Please check the server console for your OTP code.',
      data: {
        userId: user._id,
        email: user.email
      }
    });
  } catch (error) {
    console.error('Registration error:', error);
    
    // Handle duplicate key error
    if (error.code === 11000) {
      const field = Object.keys(error.keyPattern)[0];
      return res.status(400).json({
        success: false,
        message: `${field === 'email' ? 'Email' : 'Phone number'} already exists`
      });
    }
    
    // Handle validation errors
    if (error.name === 'ValidationError') {
      const messages = Object.values(error.errors).map(err => err.message);
      return res.status(400).json({
        success: false,
        message: messages.join(', ')
      });
    }
    
    res.status(500).json({
      success: false,
      message: 'Server error during registration',
      error: error.message
    });
  }
};

// @desc    Verify OTP
// @route   POST /api/auth/verify-otp
// @access  Public
exports.verifyOTP = async (req, res) => {
  try {
    const { email, otp } = req.body;

    const user = await User.findOne({ email });
    
    if (!user) {
      return res.status(404).json({
        success: false,
        message: 'User not found'
      });
    }

    if (user.isVerified) {
      return res.status(400).json({
        success: false,
        message: 'User already verified'
      });
    }

    if (!user.otp || !user.otp.code || user.otp.code !== otp) {
      return res.status(400).json({
        success: false,
        message: 'Invalid OTP'
      });
    }

    if (user.otp.expiresAt < new Date()) {
      return res.status(400).json({
        success: false,
        message: 'OTP has expired'
      });
    }

    // Update user
    user.isVerified = true;
    user.emailVerified = true;
    user.otp = undefined;
    await user.save();

    const token = generateToken(user._id);

    res.status(200).json({
      success: true,
      message: 'Email verified successfully',
      token,
      data: {
        userId: user._id,
        email: user.email,
        role: user.role,
        isVerified: user.isVerified
      }
    });
  } catch (error) {
    console.error('OTP verification error:', error);
    res.status(500).json({
      success: false,
      message: 'Server error',
      error: error.message
    });
  }
};

